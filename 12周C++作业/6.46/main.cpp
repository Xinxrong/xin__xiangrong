#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    float x,y;
    cin>>x>>y;
    cout<<"ceil("<<ceil(x)<<")"<<endl;
    cout<<"cos("<<cos(x)<<")"<<endl;
    cout<<"exp("<<exp(x)<<")"<<endl;
    cout<<"fabs("<<fabs(x)<<")"<<endl;
    cout<<"floor("<<floor(x)<<")"<<endl;
    cout<<"fmod"<<fmod(x,y)<<endl;
    cout<<"log("<<log(x)<<")"<<endl;
    cout<<"log10("<<log10(x)<<")"<<endl;
    cout<<"pow"<<pow(x,y)<<endl;
    cout<<"sin"<<sin(x)<<endl;
    cout<<"tan"<<tan(x)<<endl;
    cout<<"sqrt"<<sqrt(x)<<endl;
    return 0;
}
