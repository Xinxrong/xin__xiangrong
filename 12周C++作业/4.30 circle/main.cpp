#include <iostream>

using namespace std;

int main()
{
    double r,d,S,C;
    cout<<"Please input radius: ";
    cin>>r;
    d=2*r;
    S=3.14159*r*r;
    C=6.28318*r;
    cout<<"The diameter is: "<<d<<endl;
    cout<<"The circumference is: "<<C<<endl;
    cout<<"The Size is: "<<S<<endl;

    return 0;
}
